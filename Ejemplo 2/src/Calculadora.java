public class Calculadora {
    public Calculadora() {
    }
        public int sumar(int a, int b){
            return a+b;
        }
        public int restar(int a, int b){
            return a-b;
        }
        public int multip(int a, int b){
            return a*b;
        }
        public int divi(int a, int b){
            return a/b;

    }
}
